#include "libft.h"

void		ft_putnbr(int n)
{
	long	nbr;

	nbr = (long)n;
	if (n < 0)
	{
		ft_putchar('-');
		nbr *= (-1);
	}
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		nbr = (nbr % 10);
	}
	ft_putchar(nbr + '0');
}
